    recipe = recipe if recipe else "–ù–µ —É–∫–∞–∑–∞–Ω"
    ingredients = ingredients if ingredients else "–ù–µ —É–∫–∞–∑–∞–Ω—ã"
    
    return title, recipe, ingredients




