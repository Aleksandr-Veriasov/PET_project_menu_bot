def parse_deepseek_response(content: str):
    """–ü–∞—Ä—Å–∏—Ç –æ—Ç–≤–µ—Ç –æ—Ç DeepSeek –∏ –∏–∑–≤–ª–µ–∫–∞–µ—Ç –Ω—É–∂–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ"""
    lines = content.split("\n")
    
    title = ""
    recipe = ""
    ingredients = ""
    
    # –§–ª–∞–≥–∏ –¥–ª—è –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è —Ç–µ–∫—É—â–µ–≥–æ –±–ª–æ–∫–∞
    is_recipe = False
    is_ingredients = False
    
    for line in lines:
        # –£–¥–∞–ª—è–µ–º –ª–∏—à–Ω–∏–µ –ø—Ä–æ–±–µ–ª—ã –∏ —Å–∏–º–≤–æ–ª—ã
        line = line.strip()
        
        # –ü—Ä–æ–ø—É—Å–∫–∞–µ–º –ø—É—Å—Ç—ã–µ —Å—Ç—Ä–æ–∫–∏
        if not line:
            is_recipe = False
            continue
        
        # –ò–∑–≤–ª–µ—á–µ–Ω–∏–µ –Ω–∞–∑–≤–∞–Ω–∏—è
        if line.startswith("–ù–∞–∑–≤–∞–Ω–∏–µ —Ä–µ—Ü–µ–ø—Ç–∞:"):
            title = line.replace("–ù–∞–∑–≤–∞–Ω–∏–µ —Ä–µ—Ü–µ–ø—Ç–∞:", "").strip()
        
        # –ù–∞—á–∞–ª–æ –±–ª–æ–∫–∞ —Ä–µ—Ü–µ–ø—Ç–∞
        elif line.startswith("–†–µ—Ü–µ–ø—Ç:"):
            is_recipe = True
            recipe = line.replace("–†–µ—Ü–µ–ø—Ç:", "").strip()  # –î–æ–±–∞–≤–ª—è–µ–º —Å–∞–º —Ç–µ–∫—Å—Ç –ø–æ—Å–ª–µ "–†–µ—Ü–µ–ø—Ç:"
        
        # –ù–∞—á–∞–ª–æ –±–ª–æ–∫–∞ –∏–Ω–≥—Ä–µ–¥–∏–µ–Ω—Ç–æ–≤
        elif line.startswith("–ò–Ω–≥—Ä–µ–¥–∏–µ–Ω—Ç—ã:"):
            is_ingredients = True
            ingredients = line.replace("–ò–Ω–≥—Ä–µ–¥–∏–µ–Ω—Ç—ã:", "").strip()
        
        # –ï—Å–ª–∏ –º—ã –≤–Ω—É—Ç—Ä–∏ –±–ª–æ–∫–∞ —Ä–µ—Ü–µ–ø—Ç–∞
        elif is_recipe:
            if line.startswith("- ") or line.startswith("* ") or (line[0].isdigit() and len(line) > 1 and line[1] == "."):
                # –ï—Å–ª–∏ —Å—Ç—Ä–æ–∫–∞ –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å –º–∞—Ä–∫–µ—Ä–∞ —Å–ø–∏—Å–∫–∞ –∏–ª–∏ —Å –Ω–æ–º–µ—Ä–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä: "1.")
                recipe += "\n" + line.strip()
        
        # –ï—Å–ª–∏ –º—ã –≤–Ω—É—Ç—Ä–∏ –±–ª–æ–∫–∞ –∏–Ω–≥—Ä–µ–¥–∏–µ–Ω—Ç–æ–≤
        elif is_ingredients:
            if line.startswith("- ") or line.startswith("* ") or (line[0].isdigit() and len(line) > 1 and line[1] == "."):
                # –ï—Å–ª–∏ —Å—Ç—Ä–æ–∫–∞ –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å –º–∞—Ä–∫–µ—Ä–∞ —Å–ø–∏—Å–∫–∞ –∏–ª–∏ —Å –Ω–æ–º–µ—Ä–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä: "1.")
                ingredients += "\n" + line.strip()
    
    # –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ –ø—É—Å—Ç—ã–µ —Å—Ç—Ä–æ–∫–∏ –∏–ª–∏ —Ä–∞–∑–¥–µ–ª–µ–Ω–∏–µ –±–ª–æ–∫–æ–≤
